// `PrismModel` is the developer-facing contract introduced in
// foundation 0.3.2 per wiki ADR-020 and extended in 0.4 per ADR-035/036
// with the `R: ResolverTuple` substrate parameter: the sealed trait an
// application author implements (via the `prism_model!` macro from
// `uor-foundation-sdk`) to declare a typed route from `Input` to
// `Output`. `run_route` is the higher-level catamorphism entry point
// (ADR-022 D5) ‚Äî the canonical model-execution surface; the
// macro-emitted `forward` body delegates to it. `FoundationClosed`
// (the type-level closure witness on `Route`) and `IntoBindingValue`
// (the serialization contract on `Input` and `Output`, per ADR-023)
// are the sealed supertraits the macro emits alongside.
//
// **Input-size discipline (ADR-060, completed in foundation 0.5.1).**
// Per ADR-060 the byte width of a value carrier is an application
// concern; large structured payloads (model-weight container formats,
// multi-GB tensor-data sections, large canonical-JSON documents) are
// content-addressed by their hash, not materialized into a fixed
// buffer. Foundation 0.5.1 completed the input path:
// `IntoBindingValue::as_binding_value` returns the source-polymorphic
// `TermValue<'a, INLINE_BYTES>` carrier (`Inline` for values within the
// derived inline width, `Borrowed` for larger in-memory values,
// `Stream` for unbounded sources), and `run_route` consumes it
// directly with **no `INLINE_BYTES` cap** ‚Äî an input shape whose
// `as_binding_value` returns `Borrowed`/`Stream` flows through the
// convenience `prism_model!` path unbounded. Independently, a large
// input can be content-addressed by hash and bound directly:
// stream-hash it through the application's [`Hasher`] (`fold_bytes`,
// chunk-by-chunk, never materialized), set the leading-8-byte digest as
// an input-slot [`Binding`][crate::vocabulary::Binding]'s
// `content_address`, and drive [`run`] over a
// [`CompileUnitBuilder`][crate::vocabulary::CompileUnitBuilder] whose
// root term is `Term::Variable { name_index: 0 }`. The worked,
// replay-verified example is `tests/large_input_grounding.rs`.
pub use uor_foundation::pipeline::{run_route, FoundationClosed, IntoBindingValue, PrismModel};

