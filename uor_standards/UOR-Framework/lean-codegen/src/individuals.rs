//! Lean 4 typed individual constant generator.
//!
//! For every non-enum named individual in the ontology, emits a single
//! `def <name> : <QualifiedClass> UOR.Prims.Standard := { ... }` whose
//! field values are populated from the individual's property
//! assertions. The strict policy is: **there is no fallback to
//! bag-of-defs namespace form**. If an assertion is missing or cannot
//! be resolved at the field's declared Lean type, the gap is recorded
//! in an `UnprovenManifest` and the `lean4/individual_proof`
//! conformance check fails.
//!
//! Lake build still succeeds — defaults for unresolved fields come
//! from per-structure `Inhabited (<Name> UOR.Prims.Standard)` instances
//! generated by `structures::emit_inhabited_instance`. That keeps the
//! compilation green while conformance surfaces the gaps.

use std::collections::{BTreeMap, BTreeSet, HashMap, HashSet};
use std::fmt::Write as FmtWrite;

use serde::{Deserialize, Serialize};
use uor_ontology::model::iris::OWL_THING;
use uor_ontology::model::{
    Class, Individual, IndividualValue, NamespaceModule, Ontology, Property,
};

use crate::emit::{normalize_lean_comment, LeanFile};
use crate::mapping::{local_name, LeanNamespaceMapping};
use crate::structures::{
    compute_structure_fields, default_expr_for_type, order_sccs_by_key, tarjan_scc, EmissionScope,
    StructureField,
};

/// Reason why an individual's field could not be derived from the ontology.
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq, Eq)]
#[serde(tag = "kind", rename_all = "snake_case")]
pub enum UnprovenReason {
    /// No matching property assertion exists for this non-Option,
    /// non-Array field, and the field's Lean type has no canonical
    /// zero value derivable from the ontology.
    NoAssertion,
    /// An assertion exists but its target IRI does not resolve to a
    /// known individual or enum variant matching the field type.
    IriRefUnresolvable {
        /// The unresolved IRI from the ontology assertion.
        iri: String,
    },
    /// An assertion exists but its value type doesn't match the
    /// field's Lean type (e.g., `Str` where `Integer` is expected).
    TypeMismatch {
        /// Short name of the `IndividualValue` variant.
        value_kind: String,
        /// The Lean type the field declared.
        field_type: String,
    },
    /// A functional field's assertion points to the individual itself
    /// (non-Option cycle), or to another individual that, via a chain
    /// of assertions, forms a cycle through this field.
    CyclicSelfReference,
    /// The field type is a blocked (non-Inhabited) structure and no
    /// assertion is available. Defaults are not constructible for
    /// blocked types.
    BlockedWithoutAssertion {
        /// The blocked Lean type.
        field_type: String,
    },
}

/// Manifest of unproven individuals / fields, written to
/// `lean4/.uor-unproven.json` by the codegen and consumed by the
/// `lean4/individual_proof` conformance validator.
#[derive(Debug, Default, Clone, Serialize, Deserialize)]
pub struct UnprovenManifest {
    /// Per-field gap records: `(individual_iri, field_name, reason)`.
    /// Sorted deterministically so the JSON is byte-stable across runs.
    pub unproven_fields: Vec<UnprovenField>,
    /// Individuals whose `type_` could not be resolved to a known
    /// non-enum OWL class (ontology bug).
    pub orphan_individuals: Vec<String>,
}

/// One gap entry in the manifest.
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq, Eq)]
pub struct UnprovenField {
    /// IRI of the individual.
    pub individual: String,
    /// Name of the field (the Lean field name, keyword-escaped where
    /// applicable).
    pub field: String,
    /// Reason the field could not be derived.
    pub reason: UnprovenReason,
}

impl UnprovenManifest {
    /// Returns true iff every individual in the ontology emits
    /// entirely from its own assertions with no gaps.
    pub fn is_clean(&self) -> bool {
        self.unproven_fields.is_empty() && self.orphan_individuals.is_empty()
    }

    /// Number of distinct individuals with at least one unproven field.
    pub fn unproven_individual_count(&self) -> usize {
        let mut set: BTreeSet<&str> = BTreeSet::new();
        for f in &self.unproven_fields {
            set.insert(f.individual.as_str());
        }
        set.len()
    }

    /// Serialize to pretty JSON for the `.uor-unproven.json` file.
    pub fn to_pretty_json(&self) -> String {
        // Sort inputs for deterministic output before serializing.
        let mut fields = self.unproven_fields.clone();
        fields.sort_by(|a, b| {
            (a.individual.as_str(), a.field.as_str())
                .cmp(&(b.individual.as_str(), b.field.as_str()))
        });
        let mut orphans = self.orphan_individuals.clone();
        orphans.sort();
        let sorted = UnprovenManifest {
            unproven_fields: fields,
            orphan_individuals: orphans,
        };
        serde_json::to_string_pretty(&sorted).unwrap_or_else(|_| "{}".to_string())
    }
}

/// Result of attempting to populate one field of a typed individual.
enum FieldResolution {
    /// Proved from an ontology assertion. The String is the Lean
    /// expression to emit for this field.
    Proven(String),
    /// Proved by absence: no assertion exists but the field type
    /// (`Option T` or `Array T`) encodes explicit emptiness, so
    /// emitting `none` / `#[]` is a non-gap value.
    ProvenAbsent(String),
    /// Cannot be derived from the ontology. The String is the
    /// fallback expression (a default) the codegen emits so the Lean
    /// file still compiles, and `UnprovenReason` records the cause.
    Unproven(String, UnprovenReason),
}

/// A generated per-module individuals file.
pub struct IndividualsModuleFile {
    /// Relative path from the Lean `srcDir`, e.g. `UOR/Individuals/Op.lean`.
    pub rel_path: String,
    /// The Lean source content.
    pub content: String,
    /// The number of individuals emitted in the file.
    pub count: usize,
    /// The fully-qualified Lean module import (e.g. `UOR.Individuals.Op`).
    pub module_import: String,
}

/// Generates individual constant files, one per ontology module, plus
/// an aggregator.
///
/// Returns `(aggregator_content, per_module_files, total_count, manifest)`.
/// The manifest records every individual/field that could not be
/// derived strictly from the ontology's own assertions.
#[allow(clippy::too_many_arguments)]
pub fn generate_all_individuals<'a>(
    ontology: &'a Ontology,
    ns_map: &HashMap<&'a str, LeanNamespaceMapping>,
    all_individuals_by_iri: &HashMap<&'a str, (&'a Individual, &'a NamespaceModule)>,
    all_classes_by_iri: &HashMap<&'a str, &'a Class>,
    all_props_by_domain: &HashMap<&'a str, Vec<&'a Property>>,
    inhabited_blocked: &HashSet<&'a str>,
    skip_types: &HashSet<&'a str>,
) -> (String, Vec<IndividualsModuleFile>, usize, UnprovenManifest) {
    let mut per_module: Vec<IndividualsModuleFile> = Vec::new();
    let mut total = 0usize;
    let mut manifest = UnprovenManifest::default();

    let enum_class_names: HashSet<&str> =
        crate::enums::enum_class_names().iter().copied().collect();

    // Compute cross-module dependency graph: module M1 depends on M2
    // iff some individual in M1 has an assertion whose IriRef target
    // is an individual in M2. Cross-module references that would form
    // a cycle are forbidden (resolve as CyclicSelfReference) — the
    // rest become `import UOR.Individuals.<M2>` statements.
    let cross_module_deps =
        compute_cross_module_dependencies(ontology, ns_map, all_individuals_by_iri, skip_types);
    let forbidden_cross_refs = compute_forbidden_cross_refs(&cross_module_deps, ontology, ns_map);

    for module in &ontology.namespaces {
        let ns_iri = module.namespace.iri;
        let mapping = match ns_map.get(ns_iri) {
            Some(m) => m,
            None => continue,
        };

        let source_module_label = format!("{}.{}", mapping.space_module, mapping.file_module);

        let (inner, module_count) = generate_module_individuals(
            module,
            ns_map,
            all_individuals_by_iri,
            all_classes_by_iri,
            all_props_by_domain,
            inhabited_blocked,
            &enum_class_names,
            skip_types,
            &forbidden_cross_refs,
            &source_module_label,
            &mut manifest,
        );
        if module_count == 0 {
            continue;
        }

        let wrap_ns = format!("UOR.{}.{}", mapping.space_module, mapping.file_module);
        let file_title = format!(
            "UOR Foundation \u{2014} {} individual constants.",
            mapping.file_module
        );
        let mut f = LeanFile::new(&file_title);
        f.line("import UOR.Structures");
        // Import every other per-module individuals file this module
        // depends on (excluding forbidden cycle-breaking refs, which
        // are already recorded as Unproven above). Imports are sorted
        // for deterministic output.
        let mut deps: Vec<String> = cross_module_deps
            .get(&source_module_label)
            .cloned()
            .unwrap_or_default()
            .into_iter()
            .filter(|dep| {
                !forbidden_cross_refs.contains(&(source_module_label.clone(), dep.clone()))
            })
            .collect();
        deps.sort();
        for dep in &deps {
            let _ = writeln!(f.buf, "import UOR.Individuals.{}", module_file_of(dep));
        }
        f.blank();
        f.line("open UOR.Primitives");
        f.blank();
        let _ = writeln!(f.buf, "namespace {wrap_ns}");
        f.blank();
        f.buf.push_str(&inner);
        if !inner.ends_with('\n') {
            f.buf.push('\n');
        }
        let _ = writeln!(f.buf, "end {wrap_ns}");

        total += module_count;
        per_module.push(IndividualsModuleFile {
            rel_path: format!("UOR/Individuals/{}.lean", mapping.file_module),
            content: f.finish(),
            count: module_count,
            module_import: format!("UOR.Individuals.{}", mapping.file_module),
        });
    }

    // Aggregator file: UOR/Individuals.lean re-imports every per-module file.
    let mut agg =
        LeanFile::new("UOR Foundation \u{2014} individual constant namespaces (aggregator).");
    for entry in &per_module {
        let _ = writeln!(agg.buf, "import {}", entry.module_import);
    }

    (agg.finish(), per_module, total, manifest)
}

/// Extracts the file_module portion from a `Space.FileModule` label.
fn module_file_of(label: &str) -> &str {
    label.split('.').nth(1).unwrap_or(label)
}

/// Computes the cross-module dependency graph for individuals.
/// Returns a map: "<Space>.<FileModule>" → set of labels it depends on.
fn compute_cross_module_dependencies<'a>(
    ontology: &'a Ontology,
    ns_map: &HashMap<&'a str, LeanNamespaceMapping>,
    all_individuals_by_iri: &HashMap<&'a str, (&'a Individual, &'a NamespaceModule)>,
    skip_types: &HashSet<&str>,
) -> HashMap<String, BTreeSet<String>> {
    let mut out: HashMap<String, BTreeSet<String>> = HashMap::new();
    for module in &ontology.namespaces {
        let Some(src_mapping) = ns_map.get(module.namespace.iri) else {
            continue;
        };
        let src_label = format!("{}.{}", src_mapping.space_module, src_mapping.file_module);
        let entry = out.entry(src_label.clone()).or_default();
        for ind in &module.individuals {
            let type_local = local_name(ind.type_);
            if skip_types.contains(type_local) {
                continue;
            }
            for (_, value) in ind.properties.iter() {
                let mut targets: Vec<&str> = Vec::new();
                match value {
                    IndividualValue::IriRef(iri) => targets.push(iri),
                    IndividualValue::List(list) => {
                        for item in list.iter() {
                            targets.push(item);
                        }
                    }
                    _ => {}
                }
                for iri in targets {
                    if let Some((_, target_module)) = all_individuals_by_iri.get(iri) {
                        let Some(dst_mapping) = ns_map.get(target_module.namespace.iri) else {
                            continue;
                        };
                        let dst_label =
                            format!("{}.{}", dst_mapping.space_module, dst_mapping.file_module);
                        if dst_label != src_label {
                            entry.insert(dst_label);
                        }
                    }
                }
            }
        }
    }
    out
}

/// Computes the set of `(source_label, target_label)` pairs that
/// cannot be emitted as cross-module imports because doing so would
/// create an import cycle. For each such pair, references from
/// `source` to any individual in `target` are resolved as
/// `CyclicSelfReference` unproven gaps instead of concrete refs.
fn compute_forbidden_cross_refs<'a>(
    deps: &HashMap<String, BTreeSet<String>>,
    _ontology: &'a Ontology,
    _ns_map: &HashMap<&'a str, LeanNamespaceMapping>,
) -> HashSet<(String, String)> {
    let mut forbidden: HashSet<(String, String)> = HashSet::new();

    // Simple cycle-breaker: for each strongly-connected component in
    // the dep graph with size > 1, forbid every edge in the cycle. We
    // just find 2-cycles and larger by iterating the graph.
    let nodes: Vec<String> = deps.keys().cloned().collect();
    let node_idx: HashMap<&str, usize> = nodes
        .iter()
        .enumerate()
        .map(|(i, n)| (n.as_str(), i))
        .collect();
    let mut graph: Vec<Vec<usize>> = vec![Vec::new(); nodes.len()];
    for (src, targets) in deps {
        if let Some(&si) = node_idx.get(src.as_str()) {
            for t in targets {
                if let Some(&ti) = node_idx.get(t.as_str()) {
                    graph[si].push(ti);
                }
            }
        }
    }
    let sccs = tarjan_scc(&graph);
    for scc in &sccs {
        if scc.len() < 2 {
            continue;
        }
        let scc_set: HashSet<usize> = scc.iter().copied().collect();
        // For every edge inside the SCC, forbid: pick a deterministic
        // tiebreaker — forbid the one where source label > target
        // label, keeping the other direction.
        for &src in scc {
            for &tgt in &graph[src] {
                if scc_set.contains(&tgt) && nodes[src] > nodes[tgt] {
                    forbidden.insert((nodes[src].clone(), nodes[tgt].clone()));
                }
            }
        }
    }
    forbidden
}

/// Generates the typed `def` blocks for one module's individuals in
/// dependency order. Returns `(inner_content, count_emitted)`. Appends
/// gap entries to `manifest` as side effects.
#[allow(clippy::too_many_arguments)]
fn generate_module_individuals<'a>(
    module: &'a NamespaceModule,
    ns_map: &HashMap<&'a str, LeanNamespaceMapping>,
    all_individuals_by_iri: &HashMap<&'a str, (&'a Individual, &'a NamespaceModule)>,
    all_classes_by_iri: &HashMap<&'a str, &'a Class>,
    all_props_by_domain: &HashMap<&'a str, Vec<&'a Property>>,
    inhabited_blocked: &HashSet<&'a str>,
    enum_class_names: &HashSet<&str>,
    skip_types: &HashSet<&str>,
    forbidden_cross_refs: &HashSet<(String, String)>,
    source_module_label: &str,
    manifest: &mut UnprovenManifest,
) -> (String, usize) {
    // 1. Select non-enum individuals from this module.
    let emittable: Vec<&Individual> = module
        .individuals
        .iter()
        .filter(|ind| {
            let type_local = local_name(ind.type_);
            !skip_types.contains(type_local)
        })
        .collect();

    if emittable.is_empty() {
        return (String::new(), 0);
    }

    // 2. Build a local dependency graph over emittable individuals so
    //    cross-references resolve in topological order. Edge A -> B
    //    iff A has an assertion whose IriRef target resolves to B
    //    (and B is an in-module peer). Cross-module references are not
    //    edges in this local graph — they resolve at file import time.
    let iri_to_local_idx: HashMap<&str, usize> = emittable
        .iter()
        .enumerate()
        .map(|(i, ind)| (ind.id, i))
        .collect();
    let mut graph: Vec<Vec<usize>> = vec![Vec::new(); emittable.len()];
    for (idx, ind) in emittable.iter().enumerate() {
        let mut deps: BTreeSet<usize> = BTreeSet::new();
        for (_, value) in ind.properties.iter() {
            match value {
                IndividualValue::IriRef(target) => {
                    if let Some(&t_idx) = iri_to_local_idx.get(*target) {
                        if t_idx != idx {
                            deps.insert(t_idx);
                        }
                    }
                }
                IndividualValue::List(items) => {
                    for item in items.iter() {
                        if let Some(&t_idx) = iri_to_local_idx.get(*item) {
                            if t_idx != idx {
                                deps.insert(t_idx);
                            }
                        }
                    }
                }
                _ => {}
            }
        }
        graph[idx] = deps.into_iter().collect();
    }

    // 3. Topological order via Tarjan SCC + Kahn-by-key. Key is the
    //    individual's IRI, which is deterministic.
    let sccs = tarjan_scc(&graph);
    let emittable_ref = &emittable;
    let scc_order = order_sccs_by_key(&sccs, &graph, |node_idx| {
        emittable_ref[node_idx].id.to_string()
    });

    // Mark individuals in non-trivial SCCs as cyclic; their backward
    // references become `Unproven(CyclicSelfReference)`.
    let mut cyclic_individuals: HashSet<&str> = HashSet::new();
    for scc in &sccs {
        if scc.len() >= 2 {
            for &idx in scc {
                cyclic_individuals.insert(emittable[idx].id);
            }
        }
    }

    // 4. Emit each individual in order.
    let mut buf = String::new();
    let mut count = 0usize;
    for scc_idx in scc_order {
        let scc = &sccs[scc_idx];
        // Sort members within the SCC by IRI for stable output.
        let mut members: Vec<usize> = scc.to_vec();
        members.sort_by_key(|&i| emittable[i].id);
        for m in members {
            let ind = emittable[m];
            let inner_count = emit_typed_individual(
                &mut buf,
                ind,
                module.namespace.iri,
                ns_map,
                all_individuals_by_iri,
                all_classes_by_iri,
                all_props_by_domain,
                inhabited_blocked,
                enum_class_names,
                &cyclic_individuals,
                forbidden_cross_refs,
                source_module_label,
                manifest,
            );
            count += inner_count;
        }
    }

    (buf, count)
}

/// Emits one typed individual (or an orphan Unit placeholder). Returns
/// 1 on successful emission, 0 on skip (none currently, but reserved
/// for future filtering).
#[allow(clippy::too_many_arguments)]
fn emit_typed_individual<'a>(
    buf: &mut String,
    ind: &'a Individual,
    _module_ns_iri: &'a str,
    ns_map: &HashMap<&'a str, LeanNamespaceMapping>,
    all_individuals_by_iri: &HashMap<&'a str, (&'a Individual, &'a NamespaceModule)>,
    all_classes_by_iri: &HashMap<&'a str, &'a Class>,
    all_props_by_domain: &HashMap<&'a str, Vec<&'a Property>>,
    inhabited_blocked: &HashSet<&'a str>,
    enum_class_names: &HashSet<&str>,
    cyclic_individuals: &HashSet<&'a str>,
    forbidden_cross_refs: &HashSet<(String, String)>,
    source_module_label: &str,
    manifest: &mut UnprovenManifest,
) -> usize {
    let local = local_name(ind.id);
    let mod_name = to_individual_name(local);
    let comment = normalize_lean_comment(ind.comment);

    // Lean 4 does not accept `/-- ... -/` doc comments on `def` blocks
    // in all positions; use line comments above the def for consistency.
    let _ = writeln!(buf, "-- {comment}");

    // 1. Resolve the individual's class.
    let Some(class) = all_classes_by_iri.get(ind.type_).copied() else {
        // Orphan: unknown class. Emit Unit placeholder so the file compiles.
        let _ = writeln!(buf, "def {mod_name} : Unit := ()");
        buf.push('\n');
        manifest.orphan_individuals.push(ind.id.to_string());
        return 1;
    };

    // 2. Look up the class's owning namespace (for the qualified Lean path).
    let class_ns_iri = ns_map
        .keys()
        .copied()
        .find(|ns| ind.type_.starts_with(*ns))
        .unwrap_or("");
    let Some(class_mapping) = ns_map.get(class_ns_iri) else {
        let _ = writeln!(buf, "def {mod_name} : Unit := ()");
        buf.push('\n');
        manifest.orphan_individuals.push(ind.id.to_string());
        return 1;
    };
    let class_local = local_name(class.id);
    let qualified_class = format!(
        "UOR.{}.{}.{}",
        class_mapping.space_module, class_mapping.file_module, class_local
    );

    // 3. Compute the full field list (own + inherited parent chain),
    //    matching the struct emission order.
    let scope = EmissionScope::Namespace(class_ns_iri);
    let mut fields = compute_structure_fields(
        class,
        class_ns_iri,
        all_props_by_domain,
        all_classes_by_iri,
        ns_map,
        &scope,
        &enum_class_names_as_ref(enum_class_names),
    );
    let inherited = crate::structures::collect_all_inherited_structure_fields_public(
        class,
        class_ns_iri,
        all_props_by_domain,
        all_classes_by_iri,
        ns_map,
        &scope,
        &enum_class_names_as_ref(enum_class_names),
    );
    fields.extend(inherited);

    // 4. Group the individual's assertions by property IRI for lookup.
    let mut assertions: BTreeMap<&str, Vec<&IndividualValue>> = BTreeMap::new();
    for (prop_iri, value) in ind.properties.iter() {
        assertions.entry(*prop_iri).or_default().push(value);
    }

    // 5. Resolve each field. Track whether any field's unproven
    //    default would produce a value that Lean cannot synthesize
    //    (i.e., `default` on a blocked structure type). Such fields
    //    force the individual to an orphan Unit placeholder.
    let mut field_exprs: Vec<(String, String)> = Vec::new();
    let mut has_unsynthesizable_unproven = false;
    // If the individual's class itself is blocked, typed emission is
    // impossible unless every field is proven.
    let class_is_blocked = inhabited_blocked.contains(class.id);
    for field in &fields {
        let resolution = resolve_field(
            field,
            ind,
            &assertions,
            all_individuals_by_iri,
            all_classes_by_iri,
            ns_map,
            enum_class_names,
            inhabited_blocked,
            cyclic_individuals,
            forbidden_cross_refs,
            source_module_label,
        );
        match resolution {
            FieldResolution::Proven(expr) | FieldResolution::ProvenAbsent(expr) => {
                field_exprs.push((field.name.clone(), expr));
            }
            FieldResolution::Unproven(expr, reason) => {
                // If the class is blocked, every unproven field is
                // unsynthesizable because Lean won't construct a
                // default for any cluster-member type.
                if class_is_blocked {
                    has_unsynthesizable_unproven = true;
                }
                // If the field's Lean type is a blocked structure
                // (e.g., `GeodesicCertificate P`) and the default is
                // `default`, Lean cannot synthesize it at
                // elaboration time.
                if expr == "default" && is_blocked_type(&field.lean_type, inhabited_blocked, ns_map)
                {
                    has_unsynthesizable_unproven = true;
                }
                field_exprs.push((field.name.clone(), expr));
                manifest.unproven_fields.push(UnprovenField {
                    individual: ind.id.to_string(),
                    field: field.name.clone(),
                    reason,
                });
            }
        }
    }

    // 6. Emit. If any field produced an unsynthesizable default, fall
    //    back to a Unit placeholder and record the individual as an
    //    orphan so the `lean4/individual_proof` conformance check
    //    flags it. The file still compiles (Unit is trivially Inhabited).
    if has_unsynthesizable_unproven {
        let _ = writeln!(buf, "def {mod_name} : Unit := ()");
        buf.push('\n');
        manifest.orphan_individuals.push(ind.id.to_string());
        return 1;
    }

    let _ = writeln!(
        buf,
        "def {mod_name} : {qualified_class} UOR.Prims.Standard := {{"
    );
    for (name, expr) in &field_exprs {
        let _ = writeln!(buf, "  {name} := {expr}");
    }
    let _ = writeln!(buf, "}}");
    buf.push('\n');
    1
}

/// Bridge helper: `enum_class_names: &HashSet<&str>` → `&HashSet<&str>`.
/// Kept as a function for future evolution (e.g., lifetime adjustments).
fn enum_class_names_as_ref<'a>(s: &'a HashSet<&str>) -> HashSet<&'a str> {
    s.iter().copied().collect()
}

/// Resolves one field of an individual against its assertions.
#[allow(clippy::too_many_arguments)]
fn resolve_field<'a>(
    field: &StructureField<'_>,
    owner: &'a Individual,
    assertions: &BTreeMap<&str, Vec<&IndividualValue>>,
    all_individuals_by_iri: &HashMap<&'a str, (&'a Individual, &'a NamespaceModule)>,
    all_classes_by_iri: &HashMap<&'a str, &'a Class>,
    ns_map: &HashMap<&'a str, LeanNamespaceMapping>,
    enum_class_names: &HashSet<&str>,
    inhabited_blocked: &HashSet<&'a str>,
    cyclic_individuals: &HashSet<&'a str>,
    forbidden_cross_refs: &HashSet<(String, String)>,
    source_module_label: &str,
) -> FieldResolution {
    let lean_type = field.lean_type.as_str();
    let matching = assertions.get(field.property.id).cloned();

    match matching {
        Some(values) if !values.is_empty() => {
            // An assertion exists; try to resolve it.
            resolve_assertion_value(
                &values,
                lean_type,
                owner,
                all_individuals_by_iri,
                all_classes_by_iri,
                ns_map,
                enum_class_names,
                cyclic_individuals,
                forbidden_cross_refs,
                source_module_label,
                field.property.functional,
            )
        }
        _ => {
            // No assertion. Option and Array encode "absent" as a value.
            if lean_type.starts_with("Option ") {
                FieldResolution::ProvenAbsent("none".to_string())
            } else if lean_type.starts_with("Array ") {
                FieldResolution::ProvenAbsent("#[]".to_string())
            } else if is_blocked_type(lean_type, inhabited_blocked, ns_map) {
                FieldResolution::Unproven(
                    default_expr_for_type(lean_type),
                    UnprovenReason::BlockedWithoutAssertion {
                        field_type: lean_type.to_string(),
                    },
                )
            } else {
                FieldResolution::Unproven(
                    default_expr_for_type(lean_type),
                    UnprovenReason::NoAssertion,
                )
            }
        }
    }
}

/// Resolves an assertion `values` list against a field's Lean type.
#[allow(clippy::too_many_arguments)]
fn resolve_assertion_value<'a>(
    values: &[&IndividualValue],
    lean_type: &str,
    owner: &'a Individual,
    all_individuals_by_iri: &HashMap<&'a str, (&'a Individual, &'a NamespaceModule)>,
    all_classes_by_iri: &HashMap<&'a str, &'a Class>,
    ns_map: &HashMap<&'a str, LeanNamespaceMapping>,
    enum_class_names: &HashSet<&str>,
    cyclic_individuals: &HashSet<&'a str>,
    forbidden_cross_refs: &HashSet<(String, String)>,
    source_module_label: &str,
    functional: bool,
) -> FieldResolution {
    // Array (T P) wrapper: collect a vec of items.
    if let Some(inner) = lean_type.strip_prefix("Array ") {
        let inner = strip_outer_parens(inner);
        let mut items: Vec<String> = Vec::new();
        // Flatten any `List(..)` values and handle multi-assertion shape.
        for v in values {
            match v {
                IndividualValue::List(list) => {
                    for iri in list.iter() {
                        match resolve_iriref_target(
                            iri,
                            inner,
                            owner,
                            all_individuals_by_iri,
                            all_classes_by_iri,
                            ns_map,
                            enum_class_names,
                            cyclic_individuals,
                            forbidden_cross_refs,
                            source_module_label,
                        ) {
                            Some(expr) => items.push(expr),
                            None => {
                                return FieldResolution::Unproven(
                                    "#[]".to_string(),
                                    UnprovenReason::IriRefUnresolvable {
                                        iri: iri.to_string(),
                                    },
                                );
                            }
                        }
                    }
                }
                IndividualValue::IriRef(iri) => {
                    match resolve_iriref_target(
                        iri,
                        inner,
                        owner,
                        all_individuals_by_iri,
                        all_classes_by_iri,
                        ns_map,
                        enum_class_names,
                        cyclic_individuals,
                        forbidden_cross_refs,
                        source_module_label,
                    ) {
                        Some(expr) => items.push(expr),
                        None => {
                            return FieldResolution::Unproven(
                                "#[]".to_string(),
                                UnprovenReason::IriRefUnresolvable {
                                    iri: iri.to_string(),
                                },
                            );
                        }
                    }
                }
                IndividualValue::Str(s) if inner == "P.String" => {
                    items.push(format!("\"{}\"", escape_lean_string(s)));
                }
                IndividualValue::Int(n) if is_integer_type(inner) => {
                    items.push(format_int_for(inner, *n));
                }
                IndividualValue::Int(n) if inner == "P.Decimal" => {
                    items.push(format!("({n}.0 : Float)"));
                }
                IndividualValue::Float(x) if inner == "P.Decimal" => {
                    items.push(format!("({x} : Float)"));
                }
                IndividualValue::Bool(b) if inner == "P.Boolean" => {
                    items.push(format!("{b}"));
                }
                _ => {
                    return FieldResolution::Unproven(
                        "#[]".to_string(),
                        UnprovenReason::TypeMismatch {
                            value_kind: value_kind_name(v).to_string(),
                            field_type: lean_type.to_string(),
                        },
                    );
                }
            }
        }
        let joined = items.join(", ");
        return FieldResolution::Proven(format!("#[{joined}]"));
    }

    // Option (T P) wrapper: resolve the inner type and wrap in `some`.
    if let Some(inner) = lean_type.strip_prefix("Option ") {
        let inner = strip_outer_parens(inner);
        // Special case: RDF list flattened into a single string-valued
        // field (e.g., `op:composedOf` has range `rdf:List`, Lean type
        // `Option P.String`). Join the IRIs with `", "`.
        if inner == "P.String" {
            if let [IndividualValue::List(items)] = values {
                let joined = items.join(", ");
                return FieldResolution::Proven(format!(
                    "some (\"{}\" : String)",
                    escape_lean_string(&joined)
                ));
            }
        }
        // Single functional value expected; take the first.
        if let Some(first) = values.first() {
            // Cyclic IriRef target → proven-by-absence. The cycle
            // semantics are preserved by the ontology's own assertions;
            // Lean just cannot express the cyclic reference as a
            // finite value. `none` is the honest encoding here — not
            // a conformance gap.
            if let IndividualValue::IriRef(iri) = first {
                if let Some((target_ind, _)) = all_individuals_by_iri.get(iri).copied() {
                    if cyclic_individuals.contains(target_ind.id) || *iri == owner.id {
                        return FieldResolution::ProvenAbsent("none".to_string());
                    }
                }
            }
            match resolve_scalar_assertion(
                first,
                inner,
                owner,
                all_individuals_by_iri,
                all_classes_by_iri,
                ns_map,
                enum_class_names,
                cyclic_individuals,
                forbidden_cross_refs,
                source_module_label,
            ) {
                Some(expr) => {
                    return FieldResolution::Proven(format!("some ({expr})"));
                }
                None => {
                    return FieldResolution::Unproven(
                        "none".to_string(),
                        reason_for_scalar_mismatch(first, lean_type),
                    );
                }
            }
        }
        return FieldResolution::ProvenAbsent("none".to_string());
    }

    // Functional scalar field.
    if functional {
        if let Some(first) = values.first() {
            match resolve_scalar_assertion(
                first,
                lean_type,
                owner,
                all_individuals_by_iri,
                all_classes_by_iri,
                ns_map,
                enum_class_names,
                cyclic_individuals,
                forbidden_cross_refs,
                source_module_label,
            ) {
                Some(expr) => return FieldResolution::Proven(expr),
                None => {
                    return FieldResolution::Unproven(
                        default_expr_for_type(lean_type),
                        reason_for_scalar_mismatch(first, lean_type),
                    );
                }
            }
        }
        return FieldResolution::Unproven(
            default_expr_for_type(lean_type),
            UnprovenReason::NoAssertion,
        );
    }

    // Non-functional non-Array: shouldn't happen because the codegen
    // always wraps non-functional object properties in Array in the
    // structure declaration. Treat as type mismatch.
    FieldResolution::Unproven(
        default_expr_for_type(lean_type),
        UnprovenReason::TypeMismatch {
            value_kind: value_kind_name(
                values
                    .first()
                    .copied()
                    .unwrap_or(&IndividualValue::Bool(false)),
            )
            .to_string(),
            field_type: lean_type.to_string(),
        },
    )
}

/// Resolves a single `IndividualValue` against a non-wrapper scalar type.
#[allow(clippy::too_many_arguments)]
fn resolve_scalar_assertion<'a>(
    value: &IndividualValue,
    lean_type: &str,
    owner: &'a Individual,
    all_individuals_by_iri: &HashMap<&'a str, (&'a Individual, &'a NamespaceModule)>,
    all_classes_by_iri: &HashMap<&'a str, &'a Class>,
    ns_map: &HashMap<&'a str, LeanNamespaceMapping>,
    enum_class_names: &HashSet<&str>,
    cyclic_individuals: &HashSet<&'a str>,
    forbidden_cross_refs: &HashSet<(String, String)>,
    source_module_label: &str,
) -> Option<String> {
    match value {
        IndividualValue::Str(s) if lean_type == "P.String" => {
            Some(format!("(\"{}\" : String)", escape_lean_string(s)))
        }
        IndividualValue::Int(n) if is_integer_type(lean_type) => {
            Some(format_int_for(lean_type, *n))
        }
        IndividualValue::Int(n) if lean_type == "P.Decimal" => Some(format!("({n}.0 : Float)")),
        IndividualValue::Float(x) if lean_type == "P.Decimal" => Some(format!("({x} : Float)")),
        IndividualValue::Bool(b) if lean_type == "P.Boolean" => Some(format!("{b}")),
        IndividualValue::IriRef(iri) => resolve_iriref_target(
            iri,
            lean_type,
            owner,
            all_individuals_by_iri,
            all_classes_by_iri,
            ns_map,
            enum_class_names,
            cyclic_individuals,
            forbidden_cross_refs,
            source_module_label,
        ),
        _ => None,
    }
}

/// Maps an IRI reference to a Lean expression given the target Lean type.
///
/// Subclass coercion: when the target individual's class is a
/// transitive subclass of the field's expected type (e.g., the field
/// expects `TermExpression P` and the individual is a
/// `LiteralExpression`), the reference is emitted wrapped in a type
/// ascription `(target : ExpectedType P)`. Lean 4's elaborator
/// inserts the `.toParent` coercion automatically for `extends` chains.
#[allow(clippy::too_many_arguments)]
fn resolve_iriref_target<'a>(
    iri: &str,
    lean_type: &str,
    owner: &'a Individual,
    all_individuals_by_iri: &HashMap<&'a str, (&'a Individual, &'a NamespaceModule)>,
    all_classes_by_iri: &HashMap<&'a str, &'a Class>,
    ns_map: &HashMap<&'a str, LeanNamespaceMapping>,
    enum_class_names: &HashSet<&str>,
    cyclic_individuals: &HashSet<&'a str>,
    forbidden_cross_refs: &HashSet<(String, String)>,
    source_module_label: &str,
) -> Option<String> {
    // Self-reference → unresolvable in data form.
    if iri == owner.id {
        return None;
    }
    // P.String fallback: accept any IRI as a raw string.
    if lean_type == "P.String" {
        return Some(format!("(\"{iri}\" : String)"));
    }

    // v0.2.2 Phase D: class-IRI fallback. The BoundConstraint kind
    // individuals reference observable classes (ValueModObservable,
    // HammingMetric, ...) via boundObservable. Since the field's Lean
    // type is a class (e.g. `Observable P`), emit the class's default
    // Inhabited value. The class lookup is precise: we only fall through
    // if the IRI does not match any individual.
    if !all_individuals_by_iri.contains_key(iri) {
        if let Some(target_class) = all_classes_by_iri.get(iri) {
            let target_local = local_name(target_class.id);
            // Only accept the class-IRI fallback when the Lean field type
            // syntactically contains the class's local name OR the local
            // name of any ancestor of the class.
            let direct_match = lean_type.contains(target_local);
            let ancestor_chain = if direct_match {
                None
            } else {
                ancestor_projection_chain(lean_type, target_class.id, all_classes_by_iri)
            };
            if direct_match || ancestor_chain.is_some() {
                // Emit `(default : UOR.<Space>.<Module>.<ClassName>
                // UOR.Prims.Standard)` — Individual `def`s are monomorphic
                // at UOR.Prims.Standard; the Inhabited instance is
                // guaranteed by the structure codegen in UOR.Structures.
                // Chain `.to<Parent>` projections when the field type is
                // a strict ancestor of the target class.
                let qualified = ns_map
                    .iter()
                    .find_map(|(ns_iri, mapping)| {
                        if target_class.id.starts_with(*ns_iri) {
                            Some(format!(
                                "(default : UOR.{}.{}.{} UOR.Prims.Standard)",
                                mapping.space_module, mapping.file_module, target_local
                            ))
                        } else {
                            None
                        }
                    })
                    .unwrap_or_else(|| format!("(default : {target_local} UOR.Prims.Standard)"));
                if let Some(chain) = ancestor_chain {
                    let mut expr = qualified;
                    for step in chain {
                        expr.push_str(".to");
                        expr.push_str(step);
                    }
                    return Some(expr);
                }
                return Some(qualified);
            }
        }
    }

    let (target_ind, target_module) = all_individuals_by_iri.get(iri).copied()?;
    let target_type_local = local_name(target_ind.type_);

    // Enum variant case: the individual's type is an enum class and
    // the field's Lean type is that enum class name.
    if enum_class_names.contains(target_type_local) && lean_type == target_type_local {
        let ind_local = local_name(target_ind.id);
        // WittLevel is a `structure`, not an `inductive`, and its
        // canonical values are emitted as `def WittLevel.W8 : WittLevel := ⟨8⟩`
        // in `UOR/Enums.lean`. Reference them as `WittLevel.W8`, not `.w8`.
        if target_type_local == "WittLevel" {
            return Some(format!("WittLevel.{ind_local}"));
        }
        let variant = to_variant_name(ind_local);
        return Some(format!(".{variant}"));
    }

    // UnaryOp/BinaryOp/Involution individuals are *also* lifted into
    // the synthetic `PrimitiveOp` enum in `UOR/Enums.lean`, but we
    // additionally emit them as typed `def`s (see the individual
    // iteration filter in `generate_module_individuals`) so that
    // structure-typed references to them (e.g.,
    // `op:D2n.generatedBy → [op:neg, op:bnot]`, whose field range is
    // `op:Operation`) can resolve through the subclass coercion
    // chain `.toUnaryOp.toOperation`. No special-case skip here.

    // Cross-individual reference: the field expects a structure type.
    // Lean expression: `UOR.<Space>.<Module>.<individual_local>`.
    let mapping = ns_map.get(target_module.namespace.iri)?;
    let target_module_label = format!("{}.{}", mapping.space_module, mapping.file_module);

    // Cross-module ref forbidden: this (source, target) pair would
    // create an import cycle between individual files. Fall through
    // to Unproven.
    if target_module_label != source_module_label
        && forbidden_cross_refs
            .contains(&(source_module_label.to_string(), target_module_label.clone()))
    {
        return None;
    }

    let local_ind_name = to_individual_name(local_name(target_ind.id));
    let qualified = format!(
        "UOR.{}.{}.{}",
        mapping.space_module, mapping.file_module, local_ind_name
    );

    // Cyclic (intra-module): if the target individual is part of a
    // mutual reference cycle (e.g., `succ.inverse = pred` and
    // `pred.inverse = succ`), forward references cannot be safely
    // emitted. Break the cycle by returning None — the caller
    // substitutes `none` for `Option` fields (ProvenAbsent) or the
    // field-type default otherwise. The ontology can still carry the
    // ground-truth relationship in other properties; Lean's typed
    // emission simply cannot express a cyclic finite value.
    if cyclic_individuals.contains(target_ind.id) {
        return None;
    }
    // Match check: the field's Lean type must syntactically contain
    // the target class's local name OR the local name of any ancestor
    // of the target class (transitive subclass closure via `extends`).
    // When the match is on an ancestor rather than the target type
    // itself, emit a chain of `.toParent` projections following the
    // `extends` path. Lean 4 auto-generates `X.toImmediate` for each
    // direct parent only, so multi-level upcasts must be explicit.
    if lean_type.contains(target_type_local) {
        Some(qualified)
    } else if let Some(chain) =
        ancestor_projection_chain(lean_type, target_ind.type_, all_classes_by_iri)
    {
        let mut expr = qualified;
        for step in chain {
            expr.push_str(".to");
            expr.push_str(step);
        }
        Some(expr)
    } else {
        None
    }
}

/// Returns the list of immediate-parent local names to traverse, in
/// order, from `target_class_iri`'s direct parent down to the
/// ancestor whose local name appears in `lean_type`. For example, if
/// target is `Involution` and field type is `Operation`, returns
/// `["UnaryOp", "Operation"]`, which the caller emits as
/// `<ref>.toUnaryOp.toOperation`.
fn ancestor_projection_chain<'a>(
    lean_type: &str,
    target_class_iri: &'a str,
    all_classes_by_iri: &HashMap<&'a str, &'a Class>,
) -> Option<Vec<&'a str>> {
    // BFS along the extends chain, tracking the parent-chain path.
    let mut queue: std::collections::VecDeque<(&str, Vec<&'a str>)> =
        std::collections::VecDeque::new();
    if let Some(class) = all_classes_by_iri.get(target_class_iri) {
        for p in class.subclass_of {
            if *p == OWL_THING {
                continue;
            }
            queue.push_back((p, vec![local_name(p)]));
        }
    }
    let mut visited: HashSet<&str> = HashSet::new();
    while let Some((parent_iri, path)) = queue.pop_front() {
        if !visited.insert(parent_iri) {
            continue;
        }
        let parent_local = local_name(parent_iri);
        if lean_type.contains(parent_local) {
            return Some(path);
        }
        if let Some(parent_class) = all_classes_by_iri.get(parent_iri) {
            for gp in parent_class.subclass_of {
                if *gp == OWL_THING {
                    continue;
                }
                let mut next_path = path.clone();
                next_path.push(local_name(gp));
                queue.push_back((gp, next_path));
            }
        }
    }
    None
}

/// Returns the `UnprovenReason` for a scalar mismatch.
fn reason_for_scalar_mismatch(value: &IndividualValue, field_type: &str) -> UnprovenReason {
    match value {
        IndividualValue::IriRef(iri) => UnprovenReason::IriRefUnresolvable {
            iri: iri.to_string(),
        },
        other => UnprovenReason::TypeMismatch {
            value_kind: value_kind_name(other).to_string(),
            field_type: field_type.to_string(),
        },
    }
}

/// Tests whether a Lean type string denotes an integer primitive.
fn is_integer_type(lean_type: &str) -> bool {
    matches!(
        lean_type,
        "P.Integer" | "P.NonNegativeInteger" | "P.PositiveInteger"
    )
}

/// Formats an integer literal for a given integer-typed field.
fn format_int_for(lean_type: &str, n: i64) -> String {
    match lean_type {
        "P.Integer" => format!("({n} : Int)"),
        "P.NonNegativeInteger" => {
            let v = if n < 0 { 0 } else { n };
            format!("({v} : Nat)")
        }
        "P.PositiveInteger" => {
            let v = if n < 1 { 1 } else { n };
            format!("({v} : Nat)")
        }
        _ => format!("({n})"),
    }
}

/// Short name for `IndividualValue` variants (used in gap reasons).
fn value_kind_name(value: &IndividualValue) -> &'static str {
    match value {
        IndividualValue::Str(_) => "Str",
        IndividualValue::Int(_) => "Int",
        IndividualValue::Bool(_) => "Bool",
        IndividualValue::Float(_) => "Float",
        IndividualValue::IriRef(_) => "IriRef",
        IndividualValue::List(_) => "List",
    }
}

/// True iff `lean_type` denotes a structure type whose class IRI is in
/// `inhabited_blocked`. Used to decide whether a missing-assertion
/// default is safe to emit. This is an approximate syntactic test: it
/// scans the type string for each blocked IRI's local name.
fn is_blocked_type(
    lean_type: &str,
    inhabited_blocked: &HashSet<&str>,
    _ns_map: &HashMap<&str, LeanNamespaceMapping>,
) -> bool {
    // Primitive / wrapper types are never blocked.
    if lean_type.starts_with("P.")
        || lean_type.starts_with("Option ")
        || lean_type.starts_with("Array ")
    {
        return false;
    }
    // For structure types, check if any blocked IRI's local name
    // matches a component of the type string.
    for iri in inhabited_blocked.iter() {
        let lname = local_name(iri);
        if lean_type.contains(lname) {
            return true;
        }
    }
    false
}

/// Strip one layer of surrounding parentheses if present.
fn strip_outer_parens(s: &str) -> &str {
    let s = s.trim();
    if s.starts_with('(') && s.ends_with(')') {
        let inner = &s[1..s.len() - 1];
        if inner.chars().fold(0i32, |d, c| match c {
            '(' => d + 1,
            ')' => d - 1,
            _ => d,
        }) == 0
        {
            return inner.trim();
        }
    }
    s
}

/// Escapes a string literal for Lean 4 emission.
fn escape_lean_string(s: &str) -> String {
    s.replace('\\', "\\\\").replace('"', "\\\"")
}

/// Returns the count of non-enum individuals in a module.
pub fn count_individuals(module: &NamespaceModule, skip_types: &HashSet<&str>) -> usize {
    module
        .individuals
        .iter()
        .filter(|ind| {
            let type_local = local_name(ind.type_);
            !skip_types.contains(type_local)
        })
        .count()
}

/// Converts a local name to a Lean namespace/def name for an individual.
fn to_individual_name(s: &str) -> String {
    if is_lean_keyword(s) {
        format!("\u{ab}{s}\u{bb}")
    } else {
        s.to_string()
    }
}

/// Converts a variant-style local name to its Lean enum variant form
/// (lowercase first character). Used for `IriRef → enum variant`
/// resolution.
fn to_variant_name(s: &str) -> String {
    let mut chars = s.chars();
    match chars.next() {
        None => String::new(),
        Some(c) => {
            let mut result: String = c.to_lowercase().collect();
            result.extend(chars);
            result
        }
    }
}

/// Checks if a name is a Lean 4 keyword.
fn is_lean_keyword(s: &str) -> bool {
    matches!(
        s,
        "type"
            | "def"
            | "where"
            | "structure"
            | "class"
            | "theorem"
            | "let"
            | "do"
            | "return"
            | "match"
            | "if"
            | "else"
            | "for"
            | "in"
            | "open"
            | "namespace"
            | "end"
            | "import"
            | "mutual"
            | "variable"
            | "instance"
            | "deriving"
            | "extends"
            | "with"
            | "fun"
            | "have"
            | "show"
            | "calc"
            | "by"
            | "sorry"
            | "set_option"
            | "section"
            | "true"
            | "false"
            | "from"
            | "to"
            | "then"
            | "as"
            | "is"
            | "of"
            | "nomatch"
            | "rec"
            | "lemma"
            | "attribute"
    )
}

