    #[test]
    fn deterministic_regeneration() {
        let ontology = uor_ontology::Ontology::full();
        let ns_map = crate::mapping::lean_namespace_mappings();
        let props = crate::build_all_props_by_domain(ontology);
        let classes = crate::build_all_classes_by_iri(ontology);
        let individuals = crate::build_all_individuals_by_iri(ontology);
        let blocked = crate::structures::compute_inhabited_blocked_for_ontology(
            ontology, &ns_map, &props, &classes,
        );
        let skip = skip_types();
        let (agg_a, pm_a, _total_a, m_a) = generate_all_individuals(
            ontology,
            &ns_map,
            &individuals,
            &classes,
            &props,
            &blocked,
            &skip,
        );
        let (agg_b, pm_b, _total_b, m_b) = generate_all_individuals(
            ontology,
            &ns_map,
            &individuals,
            &classes,
            &props,
            &blocked,
            &skip,
        );
        assert_eq!(agg_a, agg_b);
        assert_eq!(pm_a.len(), pm_b.len());
        for (a, b) in pm_a.iter().zip(pm_b.iter()) {
            assert_eq!(a.content, b.content, "drift in {}", a.rel_path);
        }
        assert_eq!(m_a.to_pretty_json(), m_b.to_pretty_json());
    }
